      <p class="card-text" style="color:lightgray;">
La delega al Rappresentante Designato e le indicazioni di voto devono pervenire in una delle seguenti modalità: 
<br><br>
<u><b>in originale</u></b>, unitamente alla fotocopia di un valido documento di riconoscimento del Socio delegante (ove la sottoscrizione di quest’ultimo non sia autenticata da un componente del Consiglio di Amministrazione della Banca), in busta chiusa, sulla quale va apposta la dicitura “Delega Assemblea 2021 ChiantiBanca” entro le ore 17:00 del 26 aprile 2021 con le seguenti modalità:
<br><b>- consegna presso lo studio del rappresentante designato Notaio Dott. Marcello Focosi posto in Firenze, Viale del Poggio Imperiale n.32;
<br>- consegna presso le filiali e strutture  
<br>- inviando la busta tramite posta raccomandata al Rappresentante Designato, Notaio Dott. Marcello Focosi posto in Firenze, Viale del Poggio Imperiale n. 32, CAP 50125; 
</b><br>
<u><b>tramite posta elettronica certificata (PEC)</u></b>, completa delle istruzioni di voto e della copia del documento di riconoscimento (ove la sottoscrizione di quest’ultimo non sia autenticata da un componente del Consiglio di Amministrazione della Banca), all’indirizzo di posta elettronica certificata marcello.focosi@postacertificata.notariato.it entro le ore 23:59 dello stesso 26 aprile 2021; in tale ipotesi, la delega deve essere munita di firma elettronica qualificata o firma digitale oppure sottoscritta nella forma tradizionale su supporto cartaceo, da riprodurre in formato immagine - es. PDF - per l’allegazione al messaggio di posta elettronica certificata. 
<br><br>
La delega non ha effetto con riguardo alle proposte di deliberazione per le quali non siano state conferite istruzioni di voto. 
<br>
Le deleghe conferite al Rappresentante Designato e le relative istruzioni di voto sono revocabili con le medesime modalità ed entro gli stessi termini innanzi indicati per il conferimento delle deleghe in parola e per fornire le istruzioni di voto. 
<br>
Per eventuali richieste di chiarimento in ordine al conferimento della delega al Rappresentante Designato (e, in particolare, per la compilazione del modulo di delega, comprese le istruzioni di voto, e il relativo recapito) è possibile contattare, oltre che il Rappresentante Designato al n. 055/2337351, o via posta elettronica all’indirizzo mfocosi@notariato.it, anche la Banca al Numero Verde 800171212  (nei giorni e negli orari di apertura degli sportelli) o all’indirizzo di posta elettronica assemblea@chiantibanca.it. 
